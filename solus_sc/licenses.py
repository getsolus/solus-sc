def license_to_spdx(nom):
    global licenses
    """ Attempt to convert the name into a correct SPDX ID """
    nom = nom.strip()
    # Might already be valid
    if nom in licenses:
        return nom
    if " " in nom:
        nom = nom.replace(" ", "-")
    nom = nom.upper()
    if nom in licenses:
        return nom
    depr = "deprecated_{}".format(nom)
    if depr in licenses:
        return nom
    if nom.endswith("+"):
        nom = nom[0:-1]
    if nom in licenses:
        return nom
    return None


def spdx_to_uri(spdx):
    """ Return the SPDX URL for a given SPDX ID """
    return "https://spdx.org/licenses/{}.html".format(spdx)


